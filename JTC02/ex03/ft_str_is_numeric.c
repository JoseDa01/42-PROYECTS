#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int i;
	int cadena;

	i = 0;
	cadena = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			cadena = 0;
		i++;
	}
	return (cadena);
}
