#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	int potn;

	potn = 0;
	if (power == 0)
	{
		return (1);
	}
	else if (power < 0)
	{
		return (0);
	}
	else
	{
		potn = nb * ft_recursive_power(nb, power - 1);
		return (potn);
	}
}
